def addition(num1, num2)
  sum = num1 + num2
  return sum
end

def subtraction(num1, num2)
  minus = num1 - num2
end

def division(num1, num2)
  div = num1 / num2
end

def multiplication(num1, num2)
  multi = num1 * num2
end

def modulo(num1, num2)
  mod = num1 % num2
end

def square_root(num)
  sq_rt = Math.sqrt(num)
end
